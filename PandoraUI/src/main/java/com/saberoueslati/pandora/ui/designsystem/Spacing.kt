package com.saberoueslati.pandora.ui.designsystem

import androidx.compose.ui.unit.Dp
import androidx.compose.ui.unit.dp

object Spacing {

    /** 0.dp — no space */
    val none: Dp = 0.dp

    /** 4.dp — extra-extra-small spacing */
    val xxs: Dp = 4.dp

    /** 8.dp — extra-small spacing */
    val xs: Dp = 8.dp

    /** 12.dp — small spacing */
    val sm: Dp = 12.dp

    /** 16.dp — medium (default) spacing */
    val md: Dp = 16.dp

    /** 24.dp — large spacing */
    val lg: Dp = 24.dp

    /** 32.dp — extra-large spacing */
    val xl: Dp = 32.dp
}